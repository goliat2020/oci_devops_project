/

MERGE INTO ROL t
USING (
  SELECT 1 AS ID_ROL, 'PM' AS NOMBRE, 'Project manager' AS DESCRIPCION FROM dual
  UNION ALL SELECT 2, 'DEVELOPER', 'Developer' FROM dual
  UNION ALL SELECT 3, 'QA', 'Quality assurance' FROM dual
) s
ON (t.ID_ROL = s.ID_ROL)
WHEN NOT MATCHED THEN
  INSERT (ID_ROL, NOMBRE, DESCRIPCION)
  VALUES (s.ID_ROL, s.NOMBRE, s.DESCRIPCION);

MERGE INTO EQUIPO t
USING (
  SELECT 1 AS ID_EQUIPO, 'Equipo Principal' AS NOMBRE, DATE '2026-04-16' AS FECHA_CREACION FROM dual
) s
ON (t.ID_EQUIPO = s.ID_EQUIPO)
WHEN NOT MATCHED THEN
  INSERT (ID_EQUIPO, NOMBRE, FECHA_CREACION)
  VALUES (s.ID_EQUIPO, s.NOMBRE, s.FECHA_CREACION);

MERGE INTO ESTADO_TAREA t
USING (
  SELECT 1 AS ID_ESTADO, 'TODO' AS NOMBRE, 0 AS ES_FINAL FROM dual
  UNION ALL SELECT 2, 'EN_PROGRESO', 0 FROM dual
  UNION ALL SELECT 3, 'TERMINADA', 1 FROM dual
  UNION ALL SELECT 4, 'BLOQUEADA', 0 FROM dual
) s
ON (t.ID_ESTADO = s.ID_ESTADO)
WHEN NOT MATCHED THEN
  INSERT (ID_ESTADO, NOMBRE, ES_FINAL)
  VALUES (s.ID_ESTADO, s.NOMBRE, s.ES_FINAL);

MERGE INTO USUARIO t
USING (
  SELECT 1 AS ID_USUARIO, 'Project Manager' AS NOMBRE, 'pm@empresa.com' AS EMAIL, NULL AS TELEGRAM_CHAT_ID, 1 AS ACTIVO, DATE '2026-04-16' AS FECHA_INGRESO, 'Hibrido' AS MODALIDAD_TRABAJO, 1 AS ID_EQUIPO, 1 AS ID_ROL FROM dual
  UNION ALL SELECT 2, 'Developer 1', 'dev1@empresa.com', NULL, 1, DATE '2026-04-16', 'Remoto', 1, 2 FROM dual
  UNION ALL SELECT 3, 'Developer 2', 'dev2@empresa.com', NULL, 1, DATE '2026-04-16', 'Remoto', 1, 2 FROM dual
) s
ON (t.ID_USUARIO = s.ID_USUARIO)
WHEN NOT MATCHED THEN
  INSERT (ID_USUARIO, NOMBRE, EMAIL, TELEGRAM_CHAT_ID, ACTIVO, FECHA_INGRESO, MODALIDAD_TRABAJO, ID_EQUIPO, ID_ROL)
  VALUES (s.ID_USUARIO, s.NOMBRE, s.EMAIL, s.TELEGRAM_CHAT_ID, s.ACTIVO, s.FECHA_INGRESO, s.MODALIDAD_TRABAJO, s.ID_EQUIPO, s.ID_ROL);

MERGE INTO PROYECTO t
USING (
  SELECT 1 AS ID_PROYECTO, 'Proyecto Principal' AS NOMBRE, 'Proyecto base del dashboard' AS DESCRIPCION, 'ACTIVO' AS ESTADO, DATE '2026-04-16' AS FECHA_INICIO, DATE '2026-12-31' AS FECHA_FIN_ESTIMADA, NULL AS FECHA_FIN_REAL, 1 AS ID_EQUIPO FROM dual
) s
ON (t.ID_PROYECTO = s.ID_PROYECTO)
WHEN NOT MATCHED THEN
  INSERT (ID_PROYECTO, NOMBRE, DESCRIPCION, ESTADO, FECHA_INICIO, FECHA_FIN_ESTIMADA, FECHA_FIN_REAL, ID_EQUIPO)
  VALUES (s.ID_PROYECTO, s.NOMBRE, s.DESCRIPCION, s.ESTADO, s.FECHA_INICIO, s.FECHA_FIN_ESTIMADA, s.FECHA_FIN_REAL, s.ID_EQUIPO);

MERGE INTO SPRINT t
USING (
  SELECT 1 AS ID_SPRINT, 'Sprint 1' AS NOMBRE, 'Sprint inicial' AS OBJETIVO, DATE '2026-04-16' AS FECHA_INICIO, DATE '2026-04-30' AS FECHA_FIN, 1 AS ID_PROYECTO FROM dual
) s
ON (t.ID_SPRINT = s.ID_SPRINT)
WHEN NOT MATCHED THEN
  INSERT (ID_SPRINT, NOMBRE, OBJETIVO, FECHA_INICIO, FECHA_FIN, ID_PROYECTO)
  VALUES (s.ID_SPRINT, s.NOMBRE, s.OBJETIVO, s.FECHA_INICIO, s.FECHA_FIN, s.ID_PROYECTO);

MERGE INTO USUARIO_PROYECTO t
USING (
  SELECT 1 AS ID_USUARIO, 1 AS ID_PROYECTO, 'PM' AS ROL_EN_PROYECTO, DATE '2026-04-16' AS FECHA_ASIGNACION FROM dual
  UNION ALL SELECT 2, 1, 'DEV', DATE '2026-04-16' FROM dual
  UNION ALL SELECT 3, 1, 'DEV', DATE '2026-04-16' FROM dual
) s
ON (t.ID_USUARIO = s.ID_USUARIO AND t.ID_PROYECTO = s.ID_PROYECTO)
WHEN NOT MATCHED THEN
  INSERT (ID_USUARIO, ID_PROYECTO, ROL_EN_PROYECTO, FECHA_ASIGNACION)
  VALUES (s.ID_USUARIO, s.ID_PROYECTO, s.ROL_EN_PROYECTO, s.FECHA_ASIGNACION);

MERGE INTO TAREA t
USING (
  SELECT 1 AS ID_TAREA, 'Definir flujo principal' AS TITULO, 'Tarea de ejemplo 1' AS DESCRIPCION, DATE '2026-04-16' AS FECHA_CREACION, DATE '2026-04-20' AS FECHA_FIN_ESTIMADA, DATE '2026-04-19' AS FECHA_FIN_REAL, 6 AS ESTIMACION_HORAS, 5.5 AS HORAS_REALES, 'ALTA' AS PRIORIDAD, 2 AS ID_USUARIO, 3 AS ID_ESTADO, 1 AS ID_PROYECTO, 1 AS ID_SPRINT FROM dual
  UNION ALL SELECT 2, 'Ajustar UI del dashboard', 'Tarea de ejemplo 2', DATE '2026-04-16', DATE '2026-04-22', NULL, 4, 3.5, 'MEDIA', 3, 2, 1, 1 FROM dual
  UNION ALL SELECT 3, 'Validar KPI de tareas', 'Tarea de ejemplo 3', DATE '2026-04-16', DATE '2026-04-25', NULL, 8, NULL, 'ALTA', 2, 1, 1, 1 FROM dual
) s
ON (t.ID_TAREA = s.ID_TAREA)
WHEN NOT MATCHED THEN
  INSERT (ID_TAREA, TITULO, DESCRIPCION, FECHA_CREACION, FECHA_FIN_ESTIMADA, FECHA_FIN_REAL, ESTIMACION_HORAS, HORAS_REALES, PRIORIDAD, ID_USUARIO, ID_ESTADO, ID_PROYECTO, ID_SPRINT)
  VALUES (s.ID_TAREA, s.TITULO, s.DESCRIPCION, s.FECHA_CREACION, s.FECHA_FIN_ESTIMADA, s.FECHA_FIN_REAL, s.ESTIMACION_HORAS, s.HORAS_REALES, s.PRIORIDAD, s.ID_USUARIO, s.ID_ESTADO, s.ID_PROYECTO, s.ID_SPRINT);

MERGE INTO KPI t
USING (
  SELECT 1 AS ID_KPI, 'Tareas terminadas por usuario y sprint' AS NOMBRE, 'Conteo de tareas finalizadas por usuario y sprint' AS DESCRIPCION, 'COUNT(tareas donde estado final)' AS FORMULA_DESCRIPTIVA, 'SPRINT' AS NIVEL_APLICACION, 1 AS ID_PROYECTO FROM dual
  UNION ALL SELECT 2, 'Horas reales por usuario y sprint', 'Suma de horas reales por usuario y sprint', 'SUM(HORAS_REALES)', 'SPRINT', 1 FROM dual
) s
ON (t.ID_KPI = s.ID_KPI)
WHEN NOT MATCHED THEN
  INSERT (ID_KPI, NOMBRE, DESCRIPCION, FORMULA_DESCRIPTIVA, NIVEL_APLICACION, ID_PROYECTO)
  VALUES (s.ID_KPI, s.NOMBRE, s.DESCRIPCION, s.FORMULA_DESCRIPTIVA, s.NIVEL_APLICACION, s.ID_PROYECTO);

MERGE INTO RESULTADO_KPI t
USING (
  SELECT 1 AS ID_RESULTADO, 2 AS VALOR, DATE '2026-04-16' AS FECHA_CALCULO, 1 AS ID_KPI FROM dual
  UNION ALL SELECT 2, 9.0, DATE '2026-04-16', 2 FROM dual
) s
ON (t.ID_RESULTADO = s.ID_RESULTADO)
WHEN NOT MATCHED THEN
  INSERT (ID_RESULTADO, VALOR, FECHA_CALCULO, ID_KPI)
  VALUES (s.ID_RESULTADO, s.VALOR, s.FECHA_CALCULO, s.ID_KPI);

COMMIT;
EXIT;
