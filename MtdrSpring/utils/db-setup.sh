        PRIMARY KEY (ID_USUARIO, ID_PROYECTO),
        FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO),
        FOREIGN KEY (ID_PROYECTO) REFERENCES PROYECTO(ID_PROYECTO)
