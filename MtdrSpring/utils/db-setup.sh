BEGIN
  EXECUTE IMMEDIATE q'[
    CREATE TABLE ACTIVIDAD (
        ID_ACTIVIDAD NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
        DESCRIPCION VARCHAR2(255) NOT NULL,
        FECHA DATE,
        TIPO_ACTIVIDAD VARCHAR2(100),
        ID_USUARIO NUMBER NOT NULL,
