# Fail on error
set -e

if ! state_done WALLET_GET; then
  if test -z "$MTDRWORKSHOP_LOCATION"; then
    echo "ERROR: MTDRWORKSHOP_LOCATION is not set"
    exit
  fi
  if ! test -d "$MTDRWORKSHOP_LOCATION/wallet"; then
    echo "ERROR: wallet folder not found at $MTDRWORKSHOP_LOCATION/wallet"
    exit
  fi
  if ! test -f "$MTDRWORKSHOP_LOCATION/wallet/cwallet.sso"; then
    echo "ERROR: cwallet.sso not found in wallet folder"
    exit
  fi
  if ! test -f "$MTDRWORKSHOP_LOCATION/wallet/tnsnames.ora"; then
    echo "ERROR: tnsnames.ora not found in wallet folder"
    exit
  fi
  state_set_done OBJECT_STORE_BUCKET
  state_set_done WALLET_GET
  state_set_done CWALLET_SSO_OBJECT
  state_set_done CWALLET_SSO_AUTH_URL
fi

if [[ "${USE_EXISTING_WALLET:-0}" == "1" ]]; then
  if test -z "$EXISTING_WALLET_PATH"; then
    echo "ERROR: USE_EXISTING_WALLET is set but EXISTING_WALLET_PATH is empty"
    exit 1
  fi
  cd "$MTDRWORKSHOP_LOCATION"
  mkdir -p wallet
  if test -f "$EXISTING_WALLET_PATH"; then
    if test -n "$WALLET_ZIP_PASSWORD"; then
      unzip -o -P "$WALLET_ZIP_PASSWORD" "$EXISTING_WALLET_PATH" -d "$MTDRWORKSHOP_LOCATION/wallet"
    else
      if ! unzip -o "$EXISTING_WALLET_PATH" -d "$MTDRWORKSHOP_LOCATION/wallet"; then
        echo "ERROR: Wallet zip requires WALLET_ZIP_PASSWORD"
        exit 1
      fi
    fi
  elif test -d "$EXISTING_WALLET_PATH"; then
    cp -R "$EXISTING_WALLET_PATH"/. "$MTDRWORKSHOP_LOCATION/wallet/"
  else
    echo "ERROR: EXISTING_WALLET_PATH not found"
    exit 1
  fi
  state_set_done OBJECT_STORE_BUCKET
  state_set_done WALLET_GET
  state_set_done CWALLET_SSO_OBJECT
  state_set_done CWALLET_SSO_AUTH_URL
fi


# Wait for Order DB OCID
while ! state_done MTDR_DB_OCID; do
  echo "`date`: Waiting for MTDR_DB_OCID"
  sleep 2
done


# Give DB_PASSWORD priority
while ! state_done DB_PASSWORD; do
  echo "Waiting for DB_PASSWORD"
  sleep 5
done


# Create Inventory ATP Bindings
while ! state_done DB_WALLET_SECRET; do
  echo "creating Inventory ATP Bindings"
  cd $MTDRWORKSHOP_LOCATION/wallet
  cat - >sqlnet.ora <<!
WALLET_LOCATION = (SOURCE = (METHOD = file) (METHOD_DATA = (DIRECTORY="/mtdrworkshop/creds")))
SSL_SERVER_DN_MATCH=yes
!
  if kubectl create -f - -n mtdrworkshop; then
    state_set_done DB_WALLET_SECRET
  else
    echo "Error: Failure to create db-wallet-secret.  Retrying..."
    sleep 5
  fi <<!
apiVersion: v1
data:
  README: $(base64 -w0 README)
  cwallet.sso: $(base64 -w0 cwallet.sso)
  ewallet.p12: $(base64 -w0 ewallet.p12)
  keystore.jks: $(base64 -w0 keystore.jks)
  ojdbc.properties: $(base64 -w0 ojdbc.properties)
  sqlnet.ora: $(base64 -w0 sqlnet.ora)
  tnsnames.ora: $(base64 -w0 tnsnames.ora)
  truststore.jks: $(base64 -w0 truststore.jks)
kind: Secret
metadata:
  name: db-wallet-secret
!
  cd $MTDRWORKSHOP_LOCATION
done


# DB Connection Setup
export TNS_ADMIN=$MTDRWORKSHOP_LOCATION/wallet
cat - >$TNS_ADMIN/sqlnet.ora <<!
WALLET_LOCATION = (SOURCE = (METHOD = file) (METHOD_DATA = (DIRECTORY="$TNS_ADMIN")))
SSL_SERVER_DN_MATCH=yes
!
MTDR_DB_SVC="$(state_get MTDR_DB_NAME)_tp"
APP_USER=TODOUSER


# Get DB Password
while true; do
  if DB_PASSWORD=`kubectl get secret dbuser -n mtdrworkshop --template={{.data.dbpassword}} | base64 --decode`; then
    if ! test -z "$DB_PASSWORD"; then
      break
    fi
  fi
  echo "Error: Failed to get DB password.  Retrying..."
  sleep 5
done


# Build application schema and seed data
while ! state_done APP_SCHEMA; do
  echo "creating application schema and seed data"
  SVC=$MTDR_DB_SVC
  sqlplus -s /nolog <<SQL
WHENEVER SQLERROR EXIT 1
connect admin/"$DB_PASSWORD"@$SVC

BEGIN
  EXECUTE IMMEDIATE 'CREATE USER $APP_USER IDENTIFIED BY "$DB_PASSWORD" DEFAULT TABLESPACE data QUOTA UNLIMITED ON data';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -1920 THEN
      RAISE;
    END IF;
END;
/

GRANT CREATE SESSION TO $APP_USER;
GRANT CREATE TABLE TO $APP_USER;
GRANT CREATE VIEW TO $APP_USER;
GRANT CREATE SEQUENCE TO $APP_USER;
GRANT CREATE PROCEDURE TO $APP_USER;
GRANT CREATE TRIGGER TO $APP_USER;
GRANT CREATE TYPE TO $APP_USER;

connect $APP_USER/"$DB_PASSWORD"@$SVC

-- ========================================
-- CHECK IF SCHEMA ALREADY EXISTS
-- If tables exist, skip creation entirely
-- ========================================
DECLARE
  v_count INTEGER := 0;
BEGIN
  SELECT COUNT(*) INTO v_count FROM user_tables WHERE table_name = 'ROL';
  IF v_count > 0 THEN
    DBMS_OUTPUT.PUT_LINE('INFO: Schema already exists. Skipping table creation.');
    DBMS_OUTPUT.PUT_LINE('INFO: Database is ready to use.');
  END IF;
END;
/

-- ========================================
-- CREATE TABLES (ONLY IF THEY DO NOT EXIST)
-- ========================================

CREATE TABLE ROL (
    ID_ROL NUMBER(10) NOT NULL,
    NOMBRE VARCHAR2(100) NOT NULL,
    DESCRIPCION VARCHAR2(255),
    CONSTRAINT PK_ROL PRIMARY KEY (ID_ROL),
    CONSTRAINT UQ_ROL_NOMBRE UNIQUE (NOMBRE)
);

CREATE TABLE EQUIPO (
    ID_EQUIPO NUMBER(10) NOT NULL,
    NOMBRE VARCHAR2(150) NOT NULL,
    FECHA_CREACION DATE,
    CONSTRAINT PK_EQUIPO PRIMARY KEY (ID_EQUIPO)
);

CREATE TABLE ESTADO_TAREA (
    ID_ESTADO NUMBER(10) NOT NULL,
    NOMBRE VARCHAR2(50) NOT NULL,
    ES_FINAL NUMBER(1,0),
