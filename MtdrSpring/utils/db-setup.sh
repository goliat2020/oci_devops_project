    ID_EQUIPO NUMBER(10),
    CONSTRAINT PK_PROYECTO PRIMARY KEY (ID_PROYECTO),
    CONSTRAINT FK_PROYECTO_EQUIPO FOREIGN KEY (ID_EQUIPO) REFERENCES EQUIPO (ID_EQUIPO)
);

CREATE TABLE SPRINT (
    ID_SPRINT NUMBER(10) NOT NULL,
    NOMBRE VARCHAR2(100) NOT NULL,
    OBJETIVO VARCHAR2(500),
    FECHA_INICIO DATE,
    FECHA_FIN DATE,
    ID_PROYECTO NUMBER(10) NOT NULL,
    CONSTRAINT PK_SPRINT PRIMARY KEY (ID_SPRINT),
    CONSTRAINT FK_SPRINT_PROYECTO FOREIGN KEY (ID_PROYECTO) REFERENCES PROYECTO (ID_PROYECTO)
);

CREATE TABLE USUARIO_PROYECTO (
    ID_USUARIO NUMBER(10) NOT NULL,
    ID_PROYECTO NUMBER(10) NOT NULL,
    ROL_EN_PROYECTO VARCHAR2(100),
    FECHA_ASIGNACION DATE,
    CONSTRAINT PK_USUARIO_PROYECTO PRIMARY KEY (ID_USUARIO, ID_PROYECTO),
    CONSTRAINT FK_UP_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO),
    CONSTRAINT FK_UP_PROYECTO FOREIGN KEY (ID_PROYECTO) REFERENCES PROYECTO (ID_PROYECTO)
);

CREATE TABLE TAREA (
    ID_TAREA NUMBER(10) NOT NULL,
    TITULO VARCHAR2(200) NOT NULL,
    DESCRIPCION VARCHAR2(1000),
    FECHA_CREACION DATE,
    FECHA_FIN_ESTIMADA DATE,
    FECHA_FIN_REAL DATE,
    ESTIMACION_HORAS NUMBER(5,2),
    HORAS_REALES NUMBER(5,2),
    PRIORIDAD VARCHAR2(50),
    ID_USUARIO NUMBER(10),
    ID_ESTADO NUMBER(10) NOT NULL,
    ID_PROYECTO NUMBER(10) NOT NULL,
    ID_SPRINT NUMBER(10),
    CONSTRAINT PK_TAREA PRIMARY KEY (ID_TAREA),
    CONSTRAINT FK_TAREA_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO),
    CONSTRAINT FK_TAREA_ESTADO FOREIGN KEY (ID_ESTADO) REFERENCES ESTADO_TAREA (ID_ESTADO),
    CONSTRAINT FK_TAREA_PROYECTO FOREIGN KEY (ID_PROYECTO) REFERENCES PROYECTO (ID_PROYECTO),
    CONSTRAINT FK_TAREA_SPRINT FOREIGN KEY (ID_SPRINT) REFERENCES SPRINT (ID_SPRINT)
);

CREATE TABLE MENSAJE_CHATBOT (
    ID_MENSAJE NUMBER(10) NOT NULL,
    ID_USUARIO NUMBER(10) NOT NULL,
    TEXTO_MESSAGE VARCHAR2(1000),
    FECHA_ENVIO DATE,
    CONSTRAINT PK_MENSAJE_CHATBOT PRIMARY KEY (ID_MENSAJE),
    CONSTRAINT FK_MSG_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO)
);

CREATE TABLE ACTIVIDAD (
    ID_ACTIVIDAD NUMBER(10) NOT NULL,
    DESCRIPCION VARCHAR2(255) NOT NULL,
    FECHA DATE,
    TIPO_ACTIVIDAD VARCHAR2(100),
    ID_USUARIO NUMBER(10) NOT NULL,
    CONSTRAINT PK_ACTIVIDAD PRIMARY KEY (ID_ACTIVIDAD),
    CONSTRAINT FK_ACT_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO)
);

CREATE TABLE KPI (
    ID_KPI NUMBER(10) NOT NULL,
    NOMBRE VARCHAR2(150) NOT NULL,
    DESCRIPCION VARCHAR2(255),
    FORMULA_DESCRIPTIVA VARCHAR2(255),
    NIVEL_APLICACION VARCHAR2(100),
    ID_PROYECTO NUMBER(10) NOT NULL,
    CONSTRAINT PK_KPI PRIMARY KEY (ID_KPI),
    CONSTRAINT FK_KPI_PROYECTO FOREIGN KEY (ID_PROYECTO) REFERENCES PROYECTO (ID_PROYECTO)
);

CREATE TABLE RESULTADO_KPI (
    ID_RESULTADO NUMBER(10) NOT NULL,
    VALOR NUMBER(10,2) NOT NULL,
    FECHA_CALCULO DATE,
    ID_KPI NUMBER(10) NOT NULL,
    CONSTRAINT PK_RESULTADO_KPI PRIMARY KEY (ID_RESULTADO),
    CONSTRAINT FK_RESULTADO_KPI FOREIGN KEY (ID_KPI) REFERENCES KPI (ID_KPI)
);

CREATE INDEX IDX_USUARIO_ROL ON USUARIO (ID_ROL);
CREATE INDEX IDX_USUARIO_EQUIPO ON USUARIO (ID_EQUIPO);
CREATE INDEX IDX_PROYECTO_EQUIPO ON PROYECTO (ID_EQUIPO);
CREATE INDEX IDX_SPRINT_PROYECTO ON SPRINT (ID_PROYECTO);
CREATE INDEX IDX_UP_PROYECTO ON USUARIO_PROYECTO (ID_PROYECTO);
CREATE INDEX IDX_UP_USUARIO ON USUARIO_PROYECTO (ID_USUARIO);
CREATE INDEX IDX_TAREA_USUARIO ON TAREA (ID_USUARIO);
CREATE INDEX IDX_TAREA_ESTADO ON TAREA (ID_ESTADO);
CREATE INDEX IDX_TAREA_PROYECTO ON TAREA (ID_PROYECTO);
CREATE INDEX IDX_TAREA_SPRINT ON TAREA (ID_SPRINT);
CREATE INDEX IDX_MSG_USUARIO ON MENSAJE_CHATBOT (ID_USUARIO);
CREATE INDEX IDX_ACT_USUARIO ON ACTIVIDAD (ID_USUARIO);
CREATE INDEX IDX_KPI_PROYECTO ON KPI (ID_PROYECTO);
CREATE INDEX IDX_RESULTADO_KPI ON RESULTADO_KPI (ID_KPI);

