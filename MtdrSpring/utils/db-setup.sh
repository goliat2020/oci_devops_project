);

CREATE TABLE USUARIO (
    ID_USUARIO NUMBER(10) NOT NULL,
    NOMBRE VARCHAR2(150) NOT NULL,
    EMAIL VARCHAR2(150) NOT NULL,
    TELEGRAM_CHAT_ID VARCHAR2(50),
    ACTIVO NUMBER(1,0),
    FECHA_INGRESO DATE,
    MODALIDAD_TRABAJO VARCHAR2(50),
    ID_EQUIPO NUMBER(10),
